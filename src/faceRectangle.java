public class faceRectangle
{
	private int height;
	private int left;
	private int top;
	private int width;
	
	faceRectangle()
	{
		
	}
	
	public int getHeight()
	{
		return height;
	}
	
	public void setHeight(int h)
	{
		height = h;
	}
	
	public int setLeft()
	{
		return left;
	}
	
	public void setLeft(int l)
	{
		left = l;
	}
	
	public int getTop()
	{
		return top;
	}
	
	public void setTop(int t)
	{
		top = t;
	}
	
	public int getWidth()
	{
		return width;
	}
	
	public void setWidth(int w)
	{
		width = w;
	}

	public int getLeft() {
		return left;
	}
}
